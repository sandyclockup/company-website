<template>
    <main id="main">

    <!-- ======= Breadcrumbs ======= -->
    <section id="breadcrumbs" class="breadcrumbs">
      <div class="container">

        <div class="d-flex justify-content-between align-items-center">
          <h2>Services</h2>
          <ol>
             <li><router-link :to="{ name: 'Home'}">Home</router-link></li>
            <li>Services</li>
          </ol>
        </div>

      </div>
    </section><!-- End Breadcrumbs -->

    <!-- ======= Services Section ======= -->
    <section id="services" class="services section-bg">
      <div class="container" data-aos="fade-up">

        <div class="row">
            <div :class="serviceClasses(index)" data-aos="zoom-in" data-aos-delay="100" v-for="(service, index) in services" :key="service.id">
               <div :class="`icon-box ${randomIcon()}`">
                  <div class="icon">
                    <svg width="100" height="100" viewBox="0 0 600 600" xmlns="http://www.w3.org/2000/svg">
                      <path stroke="none" stroke-width="0" :fill="randomColor()"
                        d="M300,521.0016835830174C376.1290562159157,517.8887921683347,466.0731472004068,529.7835943286574,510.70327084640275,468.03025145048787C554.3714126377745,407.6079735673963,508.03601936045806,328.9844924480964,491.2728898941984,256.3432110539036C474.5976632858925,184.082847569629,479.9380746630129,96.60480741107993,416.23090153303,58.64404602377083C348.86323505073057,18.502131276798302,261.93793281208167,40.57373210992963,193.5410806939664,78.93577620505333C130.42746243093433,114.334589627462,98.30271207620316,179.96522072025542,76.75703585869454,249.04625023123273C51.97151888228291,328.5150500222984,13.704378332031375,421.85034740162234,66.52175969318436,486.19268352777647C119.04800174914682,550.1803526380478,217.28368757567262,524.383925680826,300,521.0016835830174">
                      </path>
                    </svg>
                    <i :class="service.icon"></i>
                  </div>
                  <h4><a href="#">{{ service.title }}</a></h4>
                  <p>{{ service.description }}</p>
                </div>
            </div>  
        </div>

      </div>
    </section><!-- End Services Section -->

    <!-- ======= Features Section ======= -->
    <section id="features" class="features">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Features</h2>
          <p>{{ config.config_feature_description }}</p>
        </div>

        <div class="row">
           <div :class="featureClasses(index)" v-for="(feature, index) in features" :key="feature.id">
              <div class="icon-box">
                <i :class="feature.icon" :style="`color: ${randomColor()};`"></i>
                <h3><a href="#">{{ feature.title }}</a></h3>
              </div>
            </div>
        </div>

      </div>
    </section><!-- End Features Section -->

  </main><!-- End #main -->
</template>
<script>
    import Swal from 'sweetalert2'
    export default {

        methods:{

            featureClasses: function(index){
                if(index === 1 || index === 2){
                  return "col-lg-3 col-md-4 mt-4 mt-md-0";
                }else if(index === 3){
                  return "col-lg-3 col-md-4 mt-4 mt-lg-0";
                }else if(index > 3){
                   return "col-lg-3 col-md-4 mt-4";
                }else{
                  return "col-lg-3 col-md-4 mt-4 mt-lg-0";
                }
            },

            serviceClasses: function(index){
              if(index === 2){
                return "col-lg-4 col-md-6 d-flex align-items-stretch mt-4 mt-lg-0";
              }else if(index > 2){
                return "col-lg-4 col-md-6 d-flex align-items-stretch mt-4";
              }else{
                return "col-lg-4 col-md-6 d-flex align-items-stretch";
              }
            },

            randomIcon: function(){
              let icons = ["iconbox-blue", "iconbox-orange", "iconbox-pink", "iconbox-yellow", "iconbox-red", "iconbox-teal"];
              let rand = icons[(Math.random() * icons.length) | 0]
              return rand;
            },

            randomColor: function(){
              return "#"+Math.floor(Math.random()*16777215).toString(16);
            },

        },

        mounted() {
            if(localStorage.getItem('config')){
                let config = JSON.parse(localStorage.getItem('config'));
                this.config = config;
                document.title = "Service | " + config.config_site_title
            }

            this.$store.dispatch("page/service").then((result) => {
                this.services = result.data;
            },
            (error) => {
                this.message =
                    (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                    error.message ||
                    error.toString();
                    Swal.fire('Sorry, something went wrong', this.message, 'error');
                }
            );

            this.$store.dispatch("page/feature").then((result) => {
                this.features = result.data;
            },
            (error) => {
                this.message =
                    (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                    error.message ||
                    error.toString();
                    Swal.fire('Sorry, something went wrong', this.message, 'error');
                }
            );


        },
        data(){
          return {
             config: {},
             services: [],
             features: []
          }
        }
    }
</script>